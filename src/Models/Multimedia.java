package Models;

/**
 *
 * @author GerardoAGL
 */
public class Multimedia {
    
    String type;
    String name; 
    String info;
    int id;

    public Multimedia(String type, String name, String info) {
        this.type = type;
        this.name = name;
        this.info = info;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }  
}
